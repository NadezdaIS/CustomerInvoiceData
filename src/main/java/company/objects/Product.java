package company.objects;

public class Product {

    private int id;
    private String name;
    private float price;

    //CONSTRUCTOR
    public Product() {

    }

    //GETTERS AND SETTERS
    public void setId(int id) {
        this.id = id;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setPrice(float price) {
        this.price = price;
    }
}
